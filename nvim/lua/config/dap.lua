local lldb = {
	name = "Launch lldb",
	type = "lldb", -- matches the adapter
	request = "launch", -- could also attach to a currently running process
	program = function()
		return vim.fn.input(
			"Path to executable: ",
			vim.fn.getcwd() .. "/",
			"file"
		)
	end,
	cwd = "${workspaceFolder}",
	stopOnEntry = false,
  args = function()
    local argument_string = vim.fn.input('Program arguments: ')
    return vim.fn.split(argument_string, " ", true)
  end,
	runInTerminal = false,
}


